function addLine(line){
	map.addSource(line.id + 'LineSource', {
		'type': 'geojson',
		'data': {
			'type': 'Feature',
			'properties': {
				name: line.id,
				lng: line.coords[0],
				lat: line.coords[1],
				zoom: line.zoom,
				boxText: line.boxText,
				boxLng: line.boxCoords[0],
				boxLat: line.boxCoords[1],
				num: line.num,
			},
			'geometry': {
				'type': 'LineString',
				'coordinates': line.data
			}
		}
	});
	map.addLayer({
		'id': line.id + 'line',
		'type': 'line',
		'source': line.id + 'LineSource',
		'paint': {
			'line-color': line.color,
			'line-width': 5,
		},
		'layout':{
			'line-join': 'round',
			'line-cap': 'round'
		}
	})
	map.addLayer({
		'id': line.id + 'clickable',
		'type': 'line',
		'source': line.id + 'LineSource',
		'paint': {
			'line-width': 30,
			'line-opacity': 0
		}
	})
	map.on('click', line.id+'clickable', function(e){
		if(!segmentFocus){
			segmentFocus = true
			currentSegment = Number(e.features[0].properties.num)
			updateButtons()
			moveTo(e.features[0].properties.lng, e.features[0].properties.lat, e.features[0].properties.zoom)
			createPopup(e.features[0].properties.boxLng, e.features[0].properties.boxLat, e.features[0].properties.boxText)
		}
	})
}

function moveTo(lng, lat, zoom){
	center = new mapboxgl.LngLat(lng, lat)
	map.flyTo({ center: center, zoom: zoom})
}

function changeView(view){
	currentView = view
	moveTo(currentView.center[0], currentView.center[1], currentView.zoom)
}

function nextSeg(change) {
	currentSegment+= change;
	updateButtons()
	if ((currentSegment <= 4 && currentView == canoeView) || (currentSegment >= 5 && currentView == bikeView)){
		currentView = mainView
	}
	line = lines[currentSegment]
	console.log(currentSegment)
	document.querySelector('#map').click()
	document.getElementsByClassName('mapboxgl-popup')[0].remove();
	moveTo(line.coords[0], line.coords[1], line.zoom)
	createPopup(line.boxCoords[0], line.boxCoords[1], line.boxText)
}

function createPopup(lng, lat, title)
{
	console.log(lng, lat, title)
	point = new mapboxgl.LngLat(lng, lat)
	new mapboxgl.Popup({
		'anchor': 'bottom',
	})
	.setLngLat(point)
	.setHTML(title)
	.addTo(map)
	.on('close', function(e){
		segmentFocus = false
		currentSegment = null
		updateButtons()
		moveTo(currentView.center[0], currentView.center[1], currentView.zoom)
	})
}

